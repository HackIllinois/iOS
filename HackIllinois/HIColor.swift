import Foundation
import UIKit

class HIColor: UIColor {
    static var paleBlue: UIColor {
        return UIColor(named: "paleBlue")!
    }

    static var hotPink: UIColor {
        return UIColor(named: "hotPink")!
    }

    static var darkIndigo: UIColor {
        return UIColor(named: "darkIndigo")!
    }

    static var lightPeriwinkle: UIColor {
        return UIColor(named: "lightPeriwinkle")!
    }

    static override var white: UIColor {
        return UIColor(named: "white")!
    }

    static var darkBlueGrey70: UIColor {
        return UIColor(named: "darkBlueGrey70")!
    }

    static var bubbleGumPink: UIColor {
        return UIColor(named: "bubbleGumPink")!
    }
}
